package com.pro.emp.domain;


public class EmpIdproofInfo {
	
	private String id;
	private String empId;
	private String attachment;
	private String description;
	private String Type;
	private String attachmentThumbnail;
	
	
	public String getAttachmentThumbnail() {
		return attachmentThumbnail;
	}
	public void setAttachmentThumbnail(String attachmentThumbnail) {
		this.attachmentThumbnail = attachmentThumbnail;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getAttachment() {
		return attachment;
	}
	public void setAttachment(String attachment) {
		this.attachment = attachment;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	

}
